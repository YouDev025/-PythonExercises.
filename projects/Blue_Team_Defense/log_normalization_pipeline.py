# Maps abbreviated month names to zero-padded numbers
_MONTHS = {
    "jan": "01", "feb": "02", "mar": "03", "apr": "04",
    "may": "05", "jun": "06", "jul": "07", "aug": "08",
    "sep": "09", "oct": "10", "nov": "11", "dec": "12",
}

def _apache_ts_to_iso(ts_str: str) -> Optional[str]:
    """
    Convert Apache Combined Log timestamp to ISO-8601 UTC.

    Input  : '10/Oct/2024:13:55:36 -0700'
    Output : '2024-10-10T20:55:36Z'
    """
    pattern = r"(\d{2})/(\w{3})/(\d{4}):(\d{2}):(\d{2}):(\d{2}) ([+-]\d{4})"
    m = re.match(pattern, ts_str.strip())
    if not m:
        return None
    day, mon, year, hh, mm, ss, tz = m.groups()
    month = _MONTHS.get(mon.lower())
    if not month:
        return None
    # Parse timezone offset into a timedelta
    sign   = 1 if tz[0] == "+" else -1
    tz_h   = int(tz[1:3])
    tz_m   = int(tz[3:5])
    offset = timedelta(hours=tz_h, minutes=tz_m) * sign
    try:
        naive  = datetime(int(year), int(month), int(day),
                          int(hh), int(mm), int(ss))
        utc_dt = naive - offset              # convert to UTC
        return utc_dt.strftime("%Y-%m-%dT%H:%M:%SZ")
    except ValueError:
        return None


def _syslog_ts_to_iso(ts_str: str, ref_year: int = None) -> Optional[str]:
    """
    Convert syslog-style timestamp to ISO-8601 UTC.

    Input  : 'Oct 10 13:55:36'
    Output : '2024-10-10T13:55:36Z'  (assumes local = UTC for syslog)

    Syslog traditionally omits the year; we default to the current year
    unless a reference year is supplied.
    """
    pattern = r"(\w{3})\s+(\d{1,2})\s+(\d{2}):(\d{2}):(\d{2})"
    m = re.match(pattern, ts_str.strip())
    if not m:
        return None
    mon, day, hh, mm, ss = m.groups()
    month = _MONTHS.get(mon.lower())
    if not month:
        return None
    year = ref_year or datetime.now(timezone.utc).year
    try:
        dt = datetime(year, int(month), int(day), int(hh), int(mm), int(ss))
        return dt.strftime("%Y-%m-%dT%H:%M:%SZ")
    except ValueError:
        return None


def _iso_passthrough(ts_str: str) -> Optional[str]:
    """
    Accept an already-ISO-ish timestamp and normalise it to UTC.

    Handles:
      2024-10-10T13:55:36Z
      2024-10-10 13:55:36
      2024-10-10T13:55:36+05:30
    """
    # Remove colon from offset so strptime can parse it
    ts_clean = re.sub(r"([+-])(\d{2}):(\d{2})$", r"\1\2\3", ts_str.strip())
    for fmt in ("%Y-%m-%dT%H:%M:%SZ",
                "%Y-%m-%dT%H:%M:%S",
                "%Y-%m-%d %H:%M:%S",
                "%Y-%m-%dT%H:%M:%S%z"):
        try:
            dt = datetime.strptime(ts_clean, fmt)
            if dt.tzinfo is None:
                dt = dt.replace(tzinfo=timezone.utc)
            utc = dt.astimezone(timezone.utc)
            return utc.strftime("%Y-%m-%dT%H:%M:%SZ")
        except ValueError:
            continue
    return None

