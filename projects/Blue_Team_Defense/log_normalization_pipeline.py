def _ts_apache(offset_hours: int = 0) -> str:
    """Generate an Apache-style timestamp offset by N hours from now."""
    dt = datetime.now(timezone.utc) - timedelta(hours=offset_hours)
    return dt.strftime("%d/%b/%Y:%H:%M:%S +0000")

def _ts_syslog(offset_hours: int = 0) -> str:
    """Generate a syslog-style timestamp offset by N hours from now."""
    dt = datetime.now(timezone.utc) - timedelta(hours=offset_hours)
    return dt.strftime("%b %d %H:%M:%S")   # no year (standard syslog)

_IPS = [
    "192.168.1.101", "10.0.0.45", "172.16.5.22",
    "203.0.113.77",  "198.51.100.3", "185.220.101.5",
]
_USERS = ["alice", "bob", "root", "admin", "charlie", "eve"]
_PATHS = [
    "/index.html", "/login", "/api/v1/users", "/admin",
    "/.env", "/wp-login.php", "/etc/passwd", "/static/app.js",
]
_HTTP_METHODS = ["GET", "POST", "PUT", "DELETE"]
_HTTP_CODES   = [200, 200, 200, 301, 302, 400, 401, 403, 404, 500, 503]
_SERVICES     = [
    "nginx.service", "sshd.service", "cron.service",
    "mysql.service", "docker.service",
]

def generate_sample_logs(count: int = 40) -> list[str]:
    """
    Produce a realistic mixed batch of raw log lines.

    Includes intentional edge cases:
     • Malformed lines
     • Missing optional fields
     • Apache logs with unusual status codes
     • Multiple failed SSH attempts (brute-force pattern)
     • OOM and disk error syslog entries
    """
    random.seed(42)   # reproducible output for demo purposes
    lines = []

    # ── Apache web logs (roughly 40 % of batch) ───────────────────
    for i in range(max(1, count // 3)):
        ip     = random.choice(_IPS)
        user   = random.choice(["-", "-", "-", random.choice(_USERS)])
        ts     = _ts_apache(random.randint(0, 24))
        method = random.choice(_HTTP_METHODS)
        path   = random.choice(_PATHS)
        code   = random.choice(_HTTP_CODES)
        size   = random.randint(200, 50000)
        lines.append(
            f'{ip} - {user} [{ts}] "{method} {path} HTTP/1.1" {code} {size}'
        )

    # ── Auth / SSH logs (roughly 40 % of batch) ──────────────────
    for i in range(max(1, count // 3)):
        ts   = _ts_syslog(random.randint(0, 24))
        host = "webserver"
        ip   = random.choice(_IPS)
        user = random.choice(_USERS)
        pid  = random.randint(1000, 9999)

        choice = random.randint(0, 5)
        if choice == 0:
            lines.append(f"{ts} {host} sshd[{pid}]: Failed password for {user} from {ip} port 22 ssh2")
        elif choice == 1:
            lines.append(f"{ts} {host} sshd[{pid}]: Accepted publickey for {user} from {ip} port 22 ssh2")
        elif choice == 2:
            lines.append(f"{ts} {host} sshd[{pid}]: Invalid user hacker from {ip} port 22")
        elif choice == 3:
            cmd = "/bin/bash"
            lines.append(f"{ts} {host} sudo: {user} : TTY=pts/0 ; PWD=/home/{user} ; USER=root ; COMMAND={cmd}")
        elif choice == 4:
            lines.append(f"{ts} {host} PAM[{pid}]: Authentication failure; logname={user} uid=1001 euid=0")
        else:
            lines.append(f"{ts} {host} sshd[{pid}]: Disconnected from {user} {ip} port 22")

    # ── Syslog / System logs (roughly 20 % of batch) ──────────────
    syslog_templates = [
        lambda ts: f"{ts} appserver kernel: [12345.678] Out of memory: Kill process {random.randint(100,9999)} (java) score {random.randint(1,999)}",
        lambda ts: f"{ts} appserver kernel: [12345.678] EXT4-fs error (device sda1): ext4_find_entry:1455: inode #{random.randint(1,999)}: comm python: reading directory lblock 0",
        lambda ts: f"{ts} appserver kernel: [12345.678] Segmentation fault at address 0x{random.randint(0,0xFFFFFFFF):08x}",
        lambda ts: f"{ts} appserver kernel: [12345.678] iptables DROPPED IN=eth0 OUT= SRC={random.choice(_IPS)} DST=10.0.0.1 PROTO=TCP DPT=22",
        lambda ts: f"{ts} appserver cron[{random.randint(500,999)}]: (root) CMD (/usr/lib/cron/run-crons)",
        lambda ts: f"{ts} appserver systemd[1]: Started {random.choice(_SERVICES)}",
        lambda ts: f"{ts} appserver systemd[1]: Failed to start {random.choice(_SERVICES)}",
        lambda ts: f"{ts} appserver systemd[1]: Stopped {random.choice(_SERVICES)}",
    ]
    for i in range(max(1, count // 5)):
        ts = _ts_syslog(random.randint(0, 24))
        lines.append(random.choice(syslog_templates)(ts))

    # ── Edge-case / malformed lines ──────────────────────────────
    lines += [
        "",                                              # blank line
        "   ",                                           # whitespace only
        "This is not a log line at all.",                # garbage
        "2024-10-10T13:55:36Z UNKNOWN EVENT XYZ",        # unknown format
        f'999.999.999.999 - - [{_ts_apache(1)}] "GET /test HTTP/1.1" 200 100',  # invalid IP
    ]

    random.shuffle(lines)
    return lines

