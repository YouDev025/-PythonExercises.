def print_normalized_record(rec: dict):
    """Pretty-print a single normalized log record."""
    sev    = rec.get("severity", "info")
    sc     = sev_colour(sev)
    valid  = rec.get("valid", False)
    v_icon = f"{C.GREEN}✓{C.RESET}" if valid else f"{C.RED}✗{C.RESET}"
    sim_tag = f"{C.DIM}(sim){C.RESET}" if rec.get("simulated") else ""

    print(f"  {C.BOLD}#{rec['id']:<4}{C.RESET}  "
          f"{sc}{sev.upper():<8}{C.RESET}  "
          f"{C.CYAN}{rec.get('source','?'):<12}{C.RESET}  "
          f"{rec.get('event_type','?'):<25}  "
          f"valid:{v_icon}  {sim_tag}")
    print(f"         ts      : {rec.get('timestamp') or C.DIM+'(missing)'+C.RESET}")
    print(f"         message : {rec.get('message','')[:90]}")
    if rec.get("ip"):
        print(f"         ip      : {rec['ip']}")
    if rec.get("user"):
        print(f"         user    : {rec['user']}")
    if rec.get("status"):
        print(f"         status  : {rec['status']}")
    print(f"         raw     : {C.DIM}{rec.get('raw','')[:90]}{C.RESET}")
    print()


def print_all_normalized(records: list[dict]):
    """Print every normalized record with a before/after comparison block."""
    if not records:
        print(f"\n  {C.YELLOW}No records to display.{C.RESET}\n")
        return

    print(f"\n{C.BOLD}{SEP2}{C.RESET}")
    print(f"  NORMALIZED LOG RECORDS  ({len(records)} records)")
    print(SEP2)
    print(f"  {'#':<5} {'SEV':<8} {'SOURCE':<12} {'EVENT TYPE':<25} {'VALID'}")
    print(f"  {SEP}")

    for rec in records:
        print_normalized_record(rec)


def print_before_after(raw_lines: list[str], records: list[dict]):
    """
    Show a side-by-side before/after transformation for the first N
    records that were successfully parsed (not 'unparsed').
    """
    SHOW = 6
    parsed_recs = [r for r in records if r.get("event_type") != "unparsed"][:SHOW]

    print(f"\n{C.BOLD}{SEP2}{C.RESET}")
    print(f"  BEFORE  vs  AFTER  (first {len(parsed_recs)} parsed records)")
    print(SEP2)

    for rec in parsed_recs:
        raw   = rec.get("raw", "")
        print(f"\n  {C.YELLOW}BEFORE{C.RESET} (raw):")
        print(f"  {C.DIM}{raw[:110]}{C.RESET}")
        print(f"\n  {C.GREEN}AFTER{C.RESET}  (normalized):")
        print_normalized_record(rec)
        print(f"  {SEP}")


def print_stats(stats: dict):
    """Display pipeline execution statistics."""
    total = stats["total_input"]

    print(f"\n{C.BOLD}{SEP2}{C.RESET}")
    print(f"  PIPELINE STATISTICS")
    print(SEP2)
    print(f"  Total input lines    : {total}")
    print(f"  Successfully parsed  : {C.GREEN}{stats['parsed']}{C.RESET}")
    print(f"  Unparsed / fallback  : {C.YELLOW}{stats['unparsed']}{C.RESET}")
    print(f"  Valid records        : {C.GREEN}{stats['valid']}{C.RESET}")
    print(f"  Invalid records      : {C.RED}{stats['invalid']}{C.RESET}")

    # Breakdown tables
    for label, bucket in [
        ("By Source",   "by_source"),
        ("By Category", "by_category"),
        ("By Severity", "by_severity"),
    ]:
        data = stats.get(bucket, {})
        if data:
            print(f"\n  {C.BOLD}{label}:{C.RESET}")
            for k, v in sorted(data.items(), key=lambda x: -x[1]):
                bar = "█" * min(v, 40)
                sc  = sev_colour(k) if bucket == "by_severity" else C.CYAN
                print(f"    {sc}{k:<18}{C.RESET}  {v:>4}  {C.DIM}{bar}{C.RESET}")

    # Validation failures
    issues = stats.get("validation_issues", [])
    if issues:
        print(f"\n  {C.BOLD}Validation Failures ({len(issues)}):{C.RESET}")
        for entry in issues[:10]:   # show max 10
            print(f"    #{entry['id']:<4}  {C.RED}{', '.join(entry['issues'])}{C.RESET}")
            print(f"           {C.DIM}{entry['raw'][:80]}{C.RESET}")
    print()

