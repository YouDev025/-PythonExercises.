def action_load_file():
    """Load raw log lines from a user-specified text file."""
    path = input("  Enter path to log file: ").strip()
    if not path:
        print(f"  {C.RED}No path entered.{C.RESET}")
        return
    if not os.path.isfile(path):
        print(f"  {C.RED}File not found: {path}{C.RESET}")
        return
    try:
        with open(path, "r", encoding="utf-8", errors="replace") as fh:
            lines = fh.readlines()
        _session.raw_lines = [l.rstrip("\n") for l in lines]
        _session.records   = []
        _session.stats     = {}
        _session.ran       = False
        print(f"\n  {C.GREEN}Loaded {len(_session.raw_lines)} lines from '{path}'.{C.RESET}\n")
    except OSError as exc:
        print(f"  {C.RED}Could not read file: {exc}{C.RESET}")


