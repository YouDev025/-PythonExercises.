# ── Apache Combined Log Format ───────────────────────────────────────
# 192.168.1.1 - frank [10/Oct/2024:13:55:36 -0700] "GET /index.html HTTP/1.1" 200 2326
_RE_APACHE = re.compile(
    r'(?P<ip>\S+)'            # client IP
    r'\s+\S+'                 # ident (usually -)
    r'\s+(?P<user>\S+)'       # authuser
    r'\s+\[(?P<ts>[^\]]+)\]'  # timestamp in brackets
    r'\s+"(?P<method>\S+)'    # HTTP method
    r'\s+(?P<path>\S+)'       # request path
    r'\s+\S+"'                # protocol
    r'\s+(?P<status>\d{3})'   # status code
    r'.*'                     # bytes + optional referrer/UA
)

def parse_apache(line: str) -> Optional[dict]:
    """
    Parse a single Apache Combined Log line.
    Returns a partially-filled record dict or None if regex fails.
    """
    m = _RE_APACHE.match(line)
    if not m:
        return None

    rec = empty_record(line)
    rec["source"]     = "web_server"
    rec["category"]   = "web"
    rec["ip"]         = m.group("ip")
    rec["user"]       = None if m.group("user") == "-" else m.group("user")
    rec["timestamp"]  = _apache_ts_to_iso(m.group("ts"))
    rec["status"]     = m.group("status")
    rec["message"]    = f'{m.group("method")} {m.group("path")} → {m.group("status")}'

    # Classify HTTP event type from method + status code
    status_int = int(m.group("status"))
    method     = m.group("method").upper()
    if status_int >= 500:
        rec["event_type"] = "http_server_error"
    elif status_int >= 400:
        rec["event_type"] = "http_client_error"
    elif method in ("POST", "PUT", "PATCH", "DELETE"):
        rec["event_type"] = "http_write"
    else:
        rec["event_type"] = "http_request"

    return rec


# ── SSH / Auth Log Format ─────────────────────────────────────────────
# Oct 10 13:55:36 webserver sshd[1234]: Failed password for root from 10.0.0.1 port 22 ssh2
# Oct 10 13:55:36 webserver sshd[1234]: Accepted publickey for alice from 10.0.0.2 port 22 ssh2
# Oct 10 13:55:36 webserver sudo: alice : TTY=pts/0 ; PWD=/home/alice ; USER=root ; COMMAND=/bin/bash
# Oct 10 13:55:36 webserver PAM[99]: Authentication failure; logname=bob uid=1001
_RE_AUTH_HEADER = re.compile(
    r"(?P<ts>\w{3}\s+\d{1,2}\s+\d{2}:\d{2}:\d{2})"  # syslog timestamp
    r"\s+\S+"                                           # hostname
    r"\s+(?P<proc>[^\[:]+)"                             # process name (sshd, sudo, PAM…)
    r"(?:\[\d+\])?"                                     # optional PID
    r":\s+(?P<msg>.+)"                                  # remainder
)
_RE_SSH_FAILED   = re.compile(r"Failed (?:password|publickey) for (?:invalid user )?(?P<user>\S+) from (?P<ip>\S+)")
_RE_SSH_ACCEPTED = re.compile(r"Accepted (?:password|publickey) for (?P<user>\S+) from (?P<ip>\S+)")
_RE_SSH_INVALID  = re.compile(r"Invalid user (?P<user>\S+) from (?P<ip>\S+)")
_RE_SUDO         = re.compile(r"(?P<user>\S+)\s+:.*COMMAND=(?P<cmd>.+)")
_RE_PAM_FAIL     = re.compile(r"Authentication failure.*logname=(?P<user>\S+)")
_RE_DISCONNECT   = re.compile(r"Disconnected from (?:invalid user )?(?P<user>\S+)?\s*(?P<ip>\d+\.\d+\.\d+\.\d+)")

def parse_auth(line: str) -> Optional[dict]:
    """Parse SSH, sudo, and PAM authentication log lines."""
    m = _RE_AUTH_HEADER.match(line)
    if not m:
        return None

    rec             = empty_record(line)
    rec["source"]   = "auth"
    rec["category"] = "auth"
    rec["timestamp"] = _syslog_ts_to_iso(m.group("ts"))
    proc            = m.group("proc").strip().lower()
    msg             = m.group("msg").strip()

    # ── sshd events ──
    if "sshd" in proc:
        fm = _RE_SSH_FAILED.search(msg)
        am = _RE_SSH_ACCEPTED.search(msg)
        im = _RE_SSH_INVALID.search(msg)
        dm = _RE_DISCONNECT.search(msg)

        if fm:
            rec["event_type"] = "login_failed"
            rec["user"]       = fm.group("user")
            rec["ip"]         = fm.group("ip")
            rec["status"]     = "failure"
            rec["message"]    = f"SSH login failed for user '{fm.group('user')}' from {fm.group('ip')}"
        elif am:
            rec["event_type"] = "login_success"
            rec["user"]       = am.group("user")
            rec["ip"]         = am.group("ip")
            rec["status"]     = "success"
            rec["message"]    = f"SSH login accepted for user '{am.group('user')}' from {am.group('ip')}"
        elif im:
            rec["event_type"] = "login_invalid_user"
            rec["user"]       = im.group("user")
            rec["ip"]         = im.group("ip")
            rec["status"]     = "failure"
            rec["message"]    = f"Invalid SSH user '{im.group('user')}' from {im.group('ip')}"
        elif dm:
            rec["event_type"] = "session_disconnect"
            rec["user"]       = dm.group("user")
            rec["ip"]         = dm.group("ip")
            rec["status"]     = "info"
            rec["message"]    = f"SSH disconnected: user='{dm.group('user')}' ip={dm.group('ip')}"
        else:
            rec["event_type"] = "ssh_event"
            rec["status"]     = "info"
            rec["message"]    = msg

    # ── sudo events ──
    elif "sudo" in proc:
        sm = _RE_SUDO.search(msg)
        if sm:
            rec["event_type"] = "privilege_escalation"
            rec["user"]       = sm.group("user")
            rec["status"]     = "success"
            rec["message"]    = f"sudo by '{sm.group('user')}': {sm.group('cmd').strip()}"
        else:
            rec["event_type"] = "sudo_event"
            rec["status"]     = "info"
            rec["message"]    = msg

    # ── PAM events ──
    elif "pam" in proc:
        pm = _RE_PAM_FAIL.search(msg)
        if pm:
            rec["event_type"] = "auth_failure"
            rec["user"]       = pm.group("user")
            rec["status"]     = "failure"
            rec["message"]    = f"PAM authentication failure for '{pm.group('user')}'"
        else:
            rec["event_type"] = "pam_event"
            rec["status"]     = "info"
            rec["message"]    = msg

    else:
        # Generic auth-header line we can at least timestamp
        rec["event_type"] = "auth_event"
        rec["status"]     = "info"
        rec["message"]    = msg

    return rec


# ── Syslog / Kernel / Application Format ─────────────────────────────
# Oct 10 13:55:36 appserver kernel: [12345.678] Out of memory: Kill process 999
# Oct 10 13:55:36 appserver cron[500]: (root) CMD (/usr/lib/cron/run-crons)
# Oct 10 13:55:36 appserver systemd[1]: Started Daily apt download activity.
# Oct 10 13:55:36 appserver CRON[1234]: pam_unix(cron:session): session opened for user root
_RE_SYSLOG = re.compile(
    r"(?P<ts>\w{3}\s+\d{1,2}\s+\d{2}:\d{2}:\d{2})"
    r"\s+(?P<host>\S+)"
    r"\s+(?P<proc>[^\[:]+)"
    r"(?:\[\d+\])?"
    r":\s+(?P<msg>.+)"
)
_RE_OOM      = re.compile(r"[Oo]ut of memory|Kill process (\d+)")
_RE_SEGFAULT = re.compile(r"segfault|Segmentation fault", re.IGNORECASE)
_RE_DISK     = re.compile(r"(I/O error|disk error|read error|write error)", re.IGNORECASE)
_RE_FIREWALL = re.compile(r"(iptables|DROPPED|REJECTED|firewall)", re.IGNORECASE)
_RE_SERVICE  = re.compile(r"(Started|Stopped|Failed|Restarting)\s+(.+)", re.IGNORECASE)

def parse_syslog(line: str) -> Optional[dict]:
    """Parse generic syslog lines (kernel, cron, systemd, etc.)."""
    m = _RE_SYSLOG.match(line)
    if not m:
        return None

    rec             = empty_record(line)
    rec["source"]   = "system"
    rec["category"] = "system"
    rec["timestamp"] = _syslog_ts_to_iso(m.group("ts"))
    proc            = m.group("proc").strip().lower()
    msg             = m.group("msg").strip()
    rec["message"]  = msg

    # Classify by process name and message content
    if "kernel" in proc:
        if _RE_OOM.search(msg):
            rec["event_type"] = "oom_kill"
            rec["status"]     = "critical"
        elif _RE_SEGFAULT.search(msg):
            rec["event_type"] = "segfault"
            rec["status"]     = "high"
        elif _RE_DISK.search(msg):
            rec["event_type"] = "disk_error"
            rec["status"]     = "high"
        elif _RE_FIREWALL.search(msg):
            rec["event_type"] = "firewall_event"
            rec["status"]     = "medium"
        else:
            rec["event_type"] = "kernel_event"
            rec["status"]     = "info"

    elif "cron" in proc:
        rec["event_type"] = "cron_job"
        rec["status"]     = "info"
        # Extract user from cron messages like (root) CMD ...
        cu = re.search(r"\((\w+)\)\s+CMD", msg)
        if cu:
            rec["user"] = cu.group(1)

    elif "systemd" in proc:
        sm = _RE_SERVICE.search(msg)
        if sm:
            action  = sm.group(1).lower()
            service = sm.group(2).strip()
            rec["event_type"] = f"service_{action}"
            rec["status"]     = "failure" if action == "failed" else "info"
            rec["message"]    = f"systemd: {action} '{service}'"
        else:
            rec["event_type"] = "systemd_event"
            rec["status"]     = "info"

    elif "NetworkManager" in m.group("proc") or "network" in proc:
        rec["event_type"] = "network_event"
        rec["category"]   = "network"
        rec["status"]     = "info"

    else:
        rec["event_type"] = "system_event"
        rec["status"]     = "info"

    return rec

# ─────────────────────────────────────────────────────────────────────
#  STAGE 2 – SOURCE DETECTION
# ─────────────────────────────────────────────────────────────────────

# Heuristics to identify log type before attempting to parse
_APACHE_HINTS = re.compile(
    r'^\S+\s+\S+\s+\S+\s+\[[^\]]+\]\s+"(?:GET|POST|PUT|DELETE|HEAD|OPTIONS|PATCH)\s'
)
_AUTH_HINTS = re.compile(
    r"\b(sshd|sudo|PAM|login|auth|passwd)\b", re.IGNORECASE
)
_SYSLOG_HINTS = re.compile(
    r"^\w{3}\s+\d{1,2}\s+\d{2}:\d{2}:\d{2}\s+\S+"
)

def detect_source(line: str) -> str:
    """
    Identify the probable source of a log line.

    Returns one of:  'apache' | 'auth' | 'syslog' | 'unknown'
    """
    if _APACHE_HINTS.match(line):
        return "apache"
    if _SYSLOG_HINTS.match(line):
        # Auth logs look like syslog but contain auth-specific process names
        if _AUTH_HINTS.search(line):
            return "auth"
        return "syslog"
    return "unknown"

# ─────────────────────────────────────────────────────────────────────
#  STAGE 4 – NORMALIZATION
# ─────────────────────────────────────────────────────────────────────

def normalize_record(rec: dict) -> dict:
    """
    Apply cross-cutting normalization rules to a parsed record.

    Rules applied:
     • Lowercase category, event_type, source
     • Strip leading/trailing whitespace from string fields
     • Replace empty strings with None
     • Validate IP address (set to None if malformed)
     • Ensure 'raw' is always a non-empty string
    """
    str_fields = ("source", "category", "event_type", "message",
                  "user", "ip", "status", "severity")
    for field in str_fields:
        val = rec.get(field)
        if isinstance(val, str):
            val = val.strip()
            rec[field] = val if val else None

    # Lowercase taxonomy fields
    for field in ("source", "category", "event_type", "severity"):
        if rec.get(field):
            rec[field] = rec[field].lower()

    # Validate IP
    if rec.get("ip") and not _valid_ip(rec["ip"]):
        rec["ip"] = None

    # Normalise hyphen-only auth user (Apache uses "-" for anonymous)
    if rec.get("user") == "-":
        rec["user"] = None

    return rec

# ─────────────────────────────────────────────────────────────────────
#  STAGE 5 – VALIDATION
# ─────────────────────────────────────────────────────────────────────

# Required fields that must be non-null for a record to be valid
_REQUIRED = ("timestamp", "source", "event_type", "message")

def validate_record(rec: dict) -> tuple:
    """
    Check a normalized record against validation rules.

    Returns (is_valid: bool, issues: list[str])
    """
    issues = []

    # Required fields
    for field in _REQUIRED:
        if not rec.get(field):
            issues.append(f"missing required field: '{field}'")

    # Timestamp format
    if rec.get("timestamp"):
        try:
            datetime.strptime(rec["timestamp"], "%Y-%m-%dT%H:%M:%SZ")
        except ValueError:
            issues.append(f"invalid timestamp format: '{rec['timestamp']}'")

    # IP validity (already normalised, but double-check)
    if rec.get("ip") and not _valid_ip(rec["ip"]):
        issues.append(f"invalid IP address: '{rec['ip']}'")

    return (len(issues) == 0, issues)

# ─────────────────────────────────────────────────────────────────────
#  STAGE 6 – TAGGING  (severity assignment)
# ─────────────────────────────────────────────────────────────────────

def assign_severity(rec: dict) -> dict:
    """
    Assign a severity label based on event type, status code, and
    message content.

    Severity ladder: critical > high > medium > low > info
    """
    et     = rec.get("event_type", "")
    status = str(rec.get("status", "") or "")
    msg    = (rec.get("message") or "").lower()

    # Critical
    if et in ("oom_kill",) or "out of memory" in msg:
        rec["severity"] = "critical"

    # High
    elif et in ("login_failed", "login_invalid_user", "auth_failure",
                "segfault", "disk_error", "http_server_error"):
        rec["severity"] = "high"
    elif status.startswith("5"):
        rec["severity"] = "high"

    # Medium
    elif et in ("http_client_error", "privilege_escalation",
                "firewall_event", "service_failed"):
        rec["severity"] = "medium"
    elif status.startswith("4"):
        rec["severity"] = "medium"

    # Low
    elif et in ("login_success", "session_disconnect", "http_write"):
        rec["severity"] = "low"

    # Info (default)
    else:
        rec["severity"] = "info"

    return rec

# ─────────────────────────────────────────────────────────────────────
#  FULL PIPELINE  (stages 2-6 chained together)
# ─────────────────────────────────────────────────────────────────────

def run_pipeline(raw_lines: list[str]) -> tuple[list[dict], dict]:
    """
    Process a list of raw log strings through all pipeline stages.

    Returns:
        records  – list of normalized record dicts
        stats    – pipeline statistics dict
    """
    records       = []
    stats = {
        "total_input":   len(raw_lines),
        "parsed":        0,
        "unparsed":      0,
        "valid":         0,
        "invalid":       0,
        "by_source":     {},
        "by_category":   {},
        "by_severity":   {},
        "validation_issues": [],
    }

    for line in raw_lines:
        line = line.rstrip("\n")
        if not line.strip():
            continue   # skip blank lines

        # ── Stage 2: detect ──────────────────────────────────────────
        src_type = detect_source(line)

        # ── Stage 3: parse ───────────────────────────────────────────
        rec = None
        if src_type == "apache":
            rec = parse_apache(line)
        elif src_type == "auth":
            rec = parse_auth(line)
        elif src_type == "syslog":
            rec = parse_syslog(line)

        if rec is None:
            # Unrecognised – create a minimal fallback record
            rec              = empty_record(line)
            rec["message"]   = line[:200]   # truncate very long lines
            rec["event_type"] = "unparsed"
            stats["unparsed"] += 1
        else:
            stats["parsed"] += 1

        # ── Stage 4: normalize ───────────────────────────────────────
        rec = normalize_record(rec)

        # ── Stage 5: validate ────────────────────────────────────────
        is_valid, issues = validate_record(rec)
        rec["valid"] = is_valid
        if not is_valid:
            stats["invalid"] += 1
            stats["validation_issues"].append({
                "id":     rec["id"],
                "raw":    line[:120],
                "issues": issues,
            })
        else:
            stats["valid"] += 1

        # ── Stage 6: tag severity ────────────────────────────────────
        rec = assign_severity(rec)

        # ── Accumulate counters ──────────────────────────────────────
        for key, bucket in [("source",   "by_source"),
                             ("category","by_category"),
                             ("severity","by_severity")]:
            label = rec.get(key, "unknown") or "unknown"
            stats[bucket][label] = stats[bucket].get(label, 0) + 1

        records.append(rec)

    return records, stats

