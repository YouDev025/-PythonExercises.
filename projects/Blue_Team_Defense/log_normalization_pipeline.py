# Counter used to assign unique IDs across the session
_id_counter = 0

def _next_id() -> int:
    global _id_counter
    _id_counter += 1
    return _id_counter

def empty_record(raw: str = "") -> dict:
    """
    Return a blank normalized record with every field set to its
    default/null value.  All parsers should start from this template
    so the schema is always complete.
    """
    return {
        "id":         _next_id(),
        "timestamp":  None,    # ISO-8601 UTC string after normalization
        "source":     "unknown",
        "category":   "unknown",
        "event_type": "unknown",
        "ip":         None,
        "user":       None,
        "message":    "",
        "status":     None,
        "severity":   "info",
        "raw":        raw,
        "valid":      False,
    }

# ─────────────────────────────────────────────────────────────────────
